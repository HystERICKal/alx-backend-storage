#!/usr/bin/env python3
"""Return all students sorted by average score."""


def top_students(mongo_collection):
    """Return all students sorted by avg score."""
    temp_1 = mongo_collection.aggregate(
        [
            {
                '$project': {
                    '_id': 1,
                    'name': 1,
                    'averageScore': {
                        '$avg': {
                            '$avg': '$topics.score',
                        },
                    },
                    'topics': 1,
                },
            },
            {
                '$sort': {'averageScore': -1},
            },
        ]
    )
    return temp_1